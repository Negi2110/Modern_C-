//cpp coding standard
//how to write a cpp code in a standard way
//1. Use meaningful variable and function names
//2. Use consistent indentation and formatting  
//3. Use comments to explain complex logic
//4. Avoid using magic numbers, use constants instead
//5. Use proper error handling and exception handling
//6. Follow the DRY (Don't Repeat Yourself) principle
//7. Use const and constexpr where appropriate
// 8. Use smart pointers instead of raw pointers
//9. Use range-based for loops and STL algorithms when possible
//10. Avoid using namespace std; in header files
//11. Use nullptr instead of NULL
//12. Use auto keyword for type inference when appropriate
//13. Use explicit keyword for single-argument constructors
//14. Use override and final keywords for virtual functions
//15. Use noexcept specifier for functions that do not throw exceptions
//16. Use static_assert for compile-time assertions

